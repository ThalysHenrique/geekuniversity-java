// CÃ¡lculos com Math
public class Programa37 {
    public static void main(String[] args) {

        System.out.println(Math.E); // Constante
        System.out.println(Math.PI);
        System.out.println(Math.sin(45));
        System.out.println(Math.cos(45));
        System.out.println(Math.tan(45));
        System.out.println(Math.round(Math.PI)); // arredonda pra cima e retorna um inteiro
        System.out.println(Math.ceil(Math.PI)); // arredonda pra cima e retorna um num real
        System.out.println(Math.floor(Math.PI)); // arredonda pra baixo e retorna um num real
        System.out.println(Math.pow(3, 2)); // Eleva o 3 ao quadrado
        System.out.println(Math.round(Math.pow(3, 2)));
        System.out.println(Math.floor(Math.pow(3, 2)));
        System.out.println(Math.sqrt(9));
    }
}
